public class OOP {

    private int age;
    private String names;
    private String course;
    private String year;

    public OOP(int age, String names, String course, String year) {
        this.age = age;
        this.names = names;
        this.course = course;
        this.year = year;
    }

    public int getAge() {
        return age;
    }

    public String getNames() {
        return names;
    }

    public String getCourse() {
        return course;
    }

    public String getYear() {
        return year;
    }
}